//switch ,case , break , default

var digit = prompt("Enter the digit");

switch (digit) {
    case "0":
        console.log("zero")
        break;
    case "1":
        console.log("one")
        break;
    case "2":
        console.log("two")
        break;
    case "3":
        console.log("There")
        break;
    case "4":
        console.log("Four")
        break;
    case "5":
        console.log("Five")
        break;
    case "6":
        console.log("six")
        break;
    case "7":
        console.log("seven")
        break;
    case "8":
        console.log("eight")
        break;
    default:
        console.log("Not a digit")
}